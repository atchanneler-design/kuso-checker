export function selectModel(inputLength: number): string {
  const estimatedTokens = Math.floor(inputLength / 2);
  if (estimatedTokens <= 1800) {
    return 'claude-haiku-4-5-20251001';
  }
  return 'claude-sonnet-4-20250514';
}
